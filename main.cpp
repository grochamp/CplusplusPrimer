#include <iostream>
using namespace std;
int main(){

    cout << "char의 크기는 " << sizeof(char) << " 이다" << endl;
    cout << "int의 크기는 " << sizeof(int) << " 이다" << endl;
    cout << "string의 크기는 " << sizeof(string) << " 이다" << endl; //32
    cout << "bool의 크기는 " << sizeof(bool) << " 이다" << endl;
    cout << "float의 크기는 " << sizeof(float) << " 이다" << endl;
    
    return 0;
}